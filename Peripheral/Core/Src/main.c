/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim == &htim3) {
		getTemp();
		if (furnaceState && temp < goal) {
			openVent();
		}
		else if (furnaceState && temp >= goal) {
			closeVent();
		}
		else if (!furnaceState && temp > goal) {
			openVent();
		}
		else if (!furnaceState && temp <= goal) {
			closeVent();
		}
	}
}
